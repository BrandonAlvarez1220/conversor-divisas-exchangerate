    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        
        if (!isWarning()) {
            
            String divisaBase = cboDivisa1.getSelectedItem().toString();
            String divisaCambio = cboDivisa2.getSelectedItem().toString();
            String cantidad = txtCantidad.getText();
            
            PeticionDivisa consulta = new PeticionDivisa();
            //Gson gson = new GsonBuilder().setFieldNamingPolicy(FieldNamingPolicy.UPPER_CAMEL_CASE).setPrettyPrinting().create();
            Divisa divisa = consulta.peticionCambio(divisaBase, divisaCambio, cantidad);
            
            
            JOptionPane.showMessageDialog(null, "La conversion es: "+divisa.conversion_result()+" "+divisaCambio);
           
        }


        
        
    }//GEN-LAST:event_btnConvertirActionPerformed

    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
       int key = evt.getKeyChar();
       boolean numeros = key>=48 && key<=57;
       
       if(!numeros)
           evt.consume();
       
    }//GEN-LAST:event_txtCantidadKeyTyped

