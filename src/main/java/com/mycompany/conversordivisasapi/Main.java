    private boolean isWarning(){
        if(cboDivisa1.getSelectedIndex() == 0 || cboDivisa2.getSelectedIndex() == 0 ){
            JOptionPane.showMessageDialog(null, "Selecciona una divisa valida");
            return true;
        }else if(txtCantidad.getText().isBlank()){
             JOptionPane.showMessageDialog(null, "Ingresa una cantidad");
             return true;
        }else
            return false;
        
    }

